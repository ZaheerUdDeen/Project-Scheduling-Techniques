    public int preDecEF(String act,String actPd)
    {
        String s1,s2;
        int es=0,es1=0,es2=0;
        for(int i=0;i<Fram.size;i++)
        {
            if(actPd.length()==1)
            {
                if(actPd.equals(mf1[i].activityName))
                {
                 if(mf1[i].preDecessorTo1==null) 
                    mf1[i].preDecessorTo1=act;
                 else
                     mf1[i].preDecessorTo2=act;
                 es=  mf1[i].ef;
                }
            }
            else if(actPd.length()==0)
            {
            }
            else
            {
                s1=actPd.substring(0,1);
                s2=actPd.substring(2,3);
                
                if(s1.equals(mf1[i].activityName))
                {
                    es1=  mf1[i].ef;
                    if(mf1[i].preDecessorTo1==null)
                        mf1[i].preDecessorTo1=act;
                   else
                       mf1[i].preDecessorTo2=act;
                }
                if(s2.equals(mf1[i].activityName))
                {
                   es2=  mf1[i].ef;
                   if(mf1[i].preDecessorTo1==null)
                        mf1[i].preDecessorTo1=act;
                   else
                       mf1[i].preDecessorTo2=act;
                }
                 es=max(es1,es2);
            }
        }
        return es;
    }
