    public int pdToFinder(String pd1,String pd2)
    {
        int lf=0,lf1=0,lf2=0;
        for(int i=0;i<Fram.size;i++)
        {
            if(pd2==null)
            {
                if(pd1.equals(mf1[i].activityName))
                {
                    lf=mf1[i].ls;
                    System.out.println(lf);
                    return lf;
                }
            }
            else
            {
                if(pd1.equals(mf1[i].activityName))
                {
                    lf1=mf1[i].ls;
                }
                if(pd2.equals(mf1[i].activityName))
                {
                    lf2=mf1[i].ls;
                }
            }
        }
   
        return min(lf1,lf2);
    }
